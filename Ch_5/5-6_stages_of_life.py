# 5-6 Stages of Life
# if-elif-else chain

age = 66

if age < 2:
    print("That's a baby!")
elif age == 2 or age < 4:
    print("That's a toddler!")
elif age == 4 or age < 13:
    print("That's a kid!")
elif age == 13 or age < 20:
    print("That's a teenager!")
elif age == 20 or age < 65:
    print("That's an adult!")
elif age >= 65:
    print("That's an old geezer!")
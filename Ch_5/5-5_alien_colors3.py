# 5-5 Alien Colors #3
# if-elif-else chain (although if-elif-elif makes more sense here)
# Three versions required, so that each variant outcome is printed

# V1

alien_color = 'green'

if alien_color == 'green':
    print("Green alien down! 5 points awarded!")
elif alien_color == 'yellow':
    print("Yellow alien down! 10 points awarded!")
elif alien_color == 'red':
    print("Red alien down! 15 points awarded!")

